.MODEL SMALL
.DATA
ad1 DW ?
ad2 DW ?
Msg1 DD 'Enter First Number :$'
Msg2 DD 10,13,'Enter Second Number :$'
Msg3 DD 10,13,'Product :$'
.CODE
.STARTUP
LEA EDX,Msg1
MOV AH,9
INT 21H

MOV EBX,0H
MOV ECX,2
loop1: SHL EBX,8
;FIRST NUMBER FIRST DIGIT INPUT
MOV AH,1
INT 21H
CMP AL,39H
JBE jump1
SUB AL,37H
jump1: AND AL,0FH
SHL AL,4
MOV BL,AL
;FIRST NUMBER SECOND DIGIT INPUT
MOV AH,1
INT 21H
CMP AL,39H
JBE jump2
SUB AL,37H
jump2: AND AL,0FH
ADD BL,AL
LOOP loop1
MOV ad1,EBX

LEA EDX,Msg2
MOV AH,9
INT 21H

MOV EBX,0H
MOV ECX,2
loop2: SHL EBX,8
;SECOND NUMBER FIRST DIGIT INPUT
MOV AH,1
INT 21H
CMP AL,39H
JBE jump3
SUB AL,37H
jump3: AND AL,0FH
SHL AL,4
MOV BL,AL
;SECOND NUMBER SECOND DIGIT INPUT
MOV AH,1
INT 21H
CMP AL,39H
JBE jump4
SUB AL,37H
jump4: AND AL,0FH
ADD BL,AL
LOOP loop2

MOV EAX,ad1
MUL EBX
MOV EBX,EDX
MOV ad1,EAX

LEA EDX,Msg3
MOV AH,9
INT 21H

MOV ECX,4
rakshit: ROL EBX,4
MOV AL,BL
AND AL,0FH
CMP AL,09H
JBE rak
ADD AL,37H
JMP shi
rak: ADD AL,30H
shi: MOV DL,AL
MOV AH,2
INT 21H
LOOP rakshit  

MOV EBX,ad1
MOV ECX,4
rakshit1: ROL EBX,4
MOV AL,BL
AND AL,0FH
CMP AL,09H
JBE rak1
ADD AL,37H
JMP shi1
rak1: ADD AL,30H
shi1: MOV DL,AL
MOV AH,2
INT 21H
LOOP rakshit1  

.EXIT
END  

